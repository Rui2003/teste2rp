<?php
$numero = 0;
switch ($numero) {

    case '$numero = 0':
        echo('Domingo');
        break;
    case '$numero = 1':
        echo('Segunda-Feira');
        break;

    case '$numero = 2':
        echo('Terca-Feira');
        break;

    case '$numero = 3':
        echo ('Quarta-Feira');
        break;

    case '$numero = 4':
        echo('Quinta-Feira');
        break;

    case '$numero = 5':
        echo('Sexta-Feira');
        break;

    case '$numero = 6':
        echo('Sábado');
        break;

    default:
        echo 'Este dia da semana não existe';
        break;
}



?>


